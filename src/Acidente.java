
public class Acidente {
	private String tipoAcidente;
	private String situacao;
	private String condTecnica;
	private String causa;
	private String predict;
	public String getTipoAcidente() {
		return tipoAcidente;
	}
	public void setTipoAcidente(String tipoAcidente) {
		this.tipoAcidente = tipoAcidente;
	}
	public String getSituacao() {
		return situacao;
	}
	public void setSituacao(String situacao) {
		this.situacao = situacao;
	}
	public String getCondTecnica() {
		return condTecnica;
	}
	public void setCondTecnica(String condTecnica) {
		this.condTecnica = condTecnica;
	}
	public String getCausa() {
		return causa;
	}
	public void setCausa(String causa) {
		this.causa = causa;
	}
	public String getPredict() {
		return predict;
	}
	public void setPredict(String predict) {
		this.predict = predict;
	}
	
	
}
